def init_scenic_data(db):
    """初始化景区数据到数据库"""
    from models import Attraction
    import json

    created_count = 0
    updated_count = 0

    for data in JIANGZHEHU_SCENIC_DATA:
        # 处理 images 和 platform_links 的序列化
        db_data = dict(data)
        if isinstance(db_data.get("images"), list):
            db_data["images"] = json.dumps(db_data["images"], ensure_ascii=False)
        if isinstance(db_data.get("platform_links"), dict):
            db_data["platform_links"] = json.dumps(db_data["platform_links"], ensure_ascii=False)

        existing = db.query(Attraction).filter(Attraction.name == db_data["name"]).first()

        if existing:
            for key, value in db_data.items():
                setattr(existing, key, value)
            existing.updated_at = datetime.utcnow()
            updated_count += 1
        else:
            attraction = Attraction(**db_data)
            db.add(attraction)
            created_count += 1

    db.commit()

    return {
        "success": True,
        "created": created_count,
        "updated": updated_count,
        "total": len(JIANGZHEHU_SCENIC_DATA),
    }


