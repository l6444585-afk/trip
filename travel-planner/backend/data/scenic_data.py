def init_scenic_data(db):
    """
    初始化景区数据到数据库
    """
    from models import Attraction
    
    created_count = 0
    updated_count = 0
    
    for data in JIANGZHEHU_SCENIC_DATA:
        existing = db.query(Attraction).filter(Attraction.name == data["name"]).first()
        
        if existing:
            for key, value in data.items():
                setattr(existing, key, value)
            existing.updated_at = datetime.utcnow()
            updated_count += 1
        else:
            attraction = Attraction(**data)
            db.add(attraction)
            created_count += 1
    
    db.commit()
    
    return {
        "success": True,
        "created": created_count,
        "updated": updated_count,
        "total": len(JIANGZHEHU_SCENIC_DATA)
    }


