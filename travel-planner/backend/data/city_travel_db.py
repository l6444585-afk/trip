def _build_merged_db():
    """合并详细数据（part1~part4）到主数据库，详细数据优先"""
    from data.city_data_part1 import CITIES_PART1
    from data.city_data_part2 import CITIES_PART2
    from data.city_data_part3 import CITIES_PART3
    from data.city_data_part4 import CITIES_PART4
    merged = dict(CITY_TRAVEL_DB)
    for source in (CITIES_PART1, CITIES_PART2, CITIES_PART3, CITIES_PART4):
        for city, data in source.items():
            if city in merged:
                merged[city] = {**merged[city], **data}
            else:
                merged[city] = data
    return merged

_MERGED_DB = _build_merged_db()


def get_city_data(city):
    """获取城市数据，优先使用详细数据，不在库中则返回杭州数据"""
    return _MERGED_DB.get(city, _MERGED_DB.get("杭州"))


def _get_season_key():
    """根据当前月份返回季节 key"""
    from datetime import datetime
    month = datetime.now().month
    if month in (3, 4, 5):
        return "spring"
    if month in (6, 7, 8):
        return "summer"
    if month in (9, 10, 11):
        return "autumn"
    return "winter"


def _find_intercity_transport(city_db, from_city):
    """从城市数据中查找城际交通信息"""
    intercity = city_db.get("intercity_transport", {})
    for key, val in intercity.items():
        if from_city in key:
            return val
    return ""


